# Exemplo 1: Usando o operador in
frutas = ["maçã", "banana", "laranja"]
print("maçã" in frutas)  # True, pois "maçã" está na lista frutas
print("uva" in frutas)   # False, pois "uva" não está na lista frutas

# Exemplo 2: Usando o operador not in
texto = "Olá, mundo!"
print("Olá" in texto)     # True, pois "Olá" está na string texto
print("Adeus" not in texto)  # True, pois "Adeus" não está na string texto
